import fs from "node:fs";
import path from "node:path";
import { getProjectFolders, readJsonFile } from "./project-workflow.mjs";

const generatedDir = path.join(process.cwd(), "src", "generated");
const registryPath = path.join(generatedDir, "projectRegistry.ts");

const toIdentifier = (name) =>
  name
    .replace(/[^a-zA-Z0-9_$]/g, "_")
    .replace(/^([^a-zA-Z_$])/, "_$1");

const projectFolders = getProjectFolders().filter(({ hasTemplate }) => hasTemplate);

fs.mkdirSync(generatedDir, { recursive: true });

const imports = [];
const entries = [];

for (const project of projectFolders) {
  const id = toIdentifier(project.name);
  const templateImport = `template_${id}`;
  const tokensImport = `tokens_${id}`;
  const videoImport = `video_${id}`;
  const template = readJsonFile(project.templatePath);

  imports.push(
    `import ${templateImport} from "../projects/${project.name}/template.json";`,
  );

  if (project.hasTokens) {
    imports.push(
      `import ${tokensImport} from "../projects/${project.name}/tokens.json";`,
    );
  }

  if (project.hasVideo) {
    imports.push(
      `import ${videoImport} from "../projects/${project.name}/video.mp4";`,
    );
  }

  entries.push(`  {
    hasTokens: ${project.hasTokens},
    hasVideo: ${project.hasVideo},
    name: ${JSON.stringify(project.name)},
    template: ${templateImport} as InfographicTemplate,
    transcriptPages: ${
      project.hasTokens ? `${tokensImport} as TranscriptPage[]` : "[]"
    },
    videoSrc: ${project.hasVideo ? videoImport : "undefined"},
    videoBased: ${template.videoBased === true},
    captions: ${template.caption === true},
  }`);
}

const contents = `/* This file is generated by scripts/generate-project-registry.mjs. */
import type { InfographicTemplate } from "../layoutCatalog";
import type { TranscriptPage } from "../video/types";
${imports.join("\n")}

export type ProjectRegistryEntry = {
  captions: boolean;
  hasTokens: boolean;
  hasVideo: boolean;
  name: string;
  template: InfographicTemplate;
  transcriptPages: TranscriptPage[];
  videoBased: boolean;
  videoSrc?: string;
};

export const projectRegistry = [
${entries.join(",\n")}
] satisfies ProjectRegistryEntry[];
`;

fs.writeFileSync(registryPath, contents, "utf8");
console.log(`Generated ${path.relative(process.cwd(), registryPath)}`);
